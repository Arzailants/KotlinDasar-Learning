fun main(){

    val data1 = 80
    val data2 = 100

    // OPERASI PERBANDINGAN >
    val logikaGreaterThan = data1 > data2
    println(logikaGreaterThan)

    // OPERASI PERBANDINGAN <
    val logikaLessThan = data1 < data2
    println(logikaLessThan)

    // OPERASI PERBANGINGAN <=
    val logikaLessThanOrEqual = data1 <= data2
    println(logikaLessThanOrEqual)

    // OPERASI PERBANDINGAN >=
    val logikaGreaterThanOrEqual = data1 >= data2
    println(logikaGreaterThan)

    // OPERASI PERBANDINGAN != / TIDAK SAMA DENGAN
    val logikaNotEqual = data1 != data2
    println(logikaNotEqual)
}