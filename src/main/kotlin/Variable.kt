fun main(){

    // VARIABLE DI KOTLIN, TERDAPAT 2
    // var = membuat variable yang mutable = bisa dirubah
    // val = membuat variable yang immutable = tidak bisa dirubah

    // MUTABLE / VAR = BISA DIRUBAH
    var data1 = "Andre Rizaldi Brillianto"
    data1 = "Alice"

    println(data1)

    // IMMUTABLE / VAL = TIDAK BISA DIRUBAH
    val dataImmutable = "Andre Rizaldi Brillianto"
//    dataImmutable = "AliceMyLove"

    println(dataImmutable)
}